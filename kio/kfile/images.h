static const QPixmap& qembed_findImage( const QString& name )
{
    static Q3Dict<QPixmap> dict;
    QPixmap* pixmap = dict.find( name );
    if ( !pixmap ) {
	for ( int i = 0; embed_image_vec[i].data; i++ ) {
	    if ( strcmp(embed_image_vec[i].name, name.latin1()) == 0 ) {
		QImage *img = new QImage((uchar*)embed_image_vec[i].data,
			    embed_image_vec[i].width,
			    embed_image_vec[i].height,
			    embed_image_vec[i].depth,
			    (QRgb*)embed_image_vec[i].colorTable,
			    embed_image_vec[i].numColors,
			    QImage::BigEndian );
		if ( embed_image_vec[i].alpha )
		    img->setAlphaBuffer( TRUE );
                pixmap = new QPixmap( *img );
                delete img;
		dict.insert( name, pixmap );
		break;
	    }
	}
	if ( !pixmap ) {
	    static QPixmap dummy;
	    return dummy;
	}
    }
    return *pixmap;
}

